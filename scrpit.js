function sumNumber(arg){
    let counter = 0;
    let res = 0;
    for(let i = arg; i>=1; i/=10){
        counter++
    }
    for(let i = 1; i <= counter; i++){
        let num = Math.trunc((arg/10**(i-1)))%10
        res += num
    }
    return res
}

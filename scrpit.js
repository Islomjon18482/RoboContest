const meduim = (arr) => {
    let array = arr.split(" ").map(parseFloat);
    let max = array[0];
    let min = array[0];
    let counter = 0;
    let sum = 0;
    array.forEach(element => {
        counter++
        sum+=element
        if (max < element) {
            max = element
        }
        if (min > element) {
            min = element
        }
    });
    let res = (sum - max-min)/(counter-2)
    return res
}
